# for loops-its used to run a block of code for a certain number of times
# 1. loop form 1 to 10
for count in range(1,10,1):
    print(count)



# 2. loop from 100,200 
for number in range(100,200,1):
    print(number)


# 3. loop from 50 to -50 
for number in range(50,-50,-1):
    print(number)


# 4. loop even numbers form 100 to 200 
for number in range(100,200,2):
    print(number)