# python list- is an collection of organised items.
cars=["Honda","Mazda","Surf","Volkswagon","Mercedes","Nissan"]
# show output
print(cars)
# print item at index 2
print(cars[2])
# append BMW 
cars.append("BMW")
# show output
print(cars)
# insert toyota at index 2

cars.insert(2,"toyota")
# show output
print(cars)
# slicing
# 1. print items form index 2 upto 5
print(cars[2:5])
# 2. print form index 3 and above
print(cars[3:])
# 3. print upto index 4 and below
print(cars[:4])