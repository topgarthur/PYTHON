# 1. Simple Interest Calculation
def simple_interest(p, r, t):
    answer = p * r * t / 100
    print("The interest is", answer)
simple_interest(25000, 15, 4)

# bmi
def bmi(w,h):
    answer=w/(h**2)
    print("the bmi is",answer)
# call the function 
bmi(70,1.74)    

